package com.vaishnavi.loop.pattern.doWhileLoop;

public class DoWhileLoopDemo {
    //pseudocode
    //print no 1 to 5.using dp while loop.
    public static void main(String[] args) {
        int i = 0;
        do {
            System.out.println(i);
            i++;
        }
        while (i <= 5);
    }
}
