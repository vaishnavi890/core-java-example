package com.vaishnavi.practice.array;

public class ArrayPractice {
    public static void main(String[] args) {
        String[] name = new String[5];
        name[0] = "shreya";
        name[1] = "mahalle";
        name[2] = "amt";
        name[3] = "pune";
        name[4] = "mumbai";

        for (String string : name) System.out.println(string);
    }
}
